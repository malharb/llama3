@dataclass
class ModelArgs:
    dim: int = 4096
    n_layers: int = 32
    n_heads: int = 32
    n_kv_heads: Optional[int] = None
    vocab_size: int = -1
    multiple_of: int = 256  # make SwiGLU hidden layer size multiple of large power of 2
    ffn_dim_multiplier: Optional[float] = None
    norm_eps: float = 1e-5
    rope_theta: float = 500000

    max_batch_size: int = 32
    max_seq_len: int = 2048


class RMSNorm(torch.nn.Module):
    def __init__(self, dim: int, eps: float = 1e-6):
        super().__init__()
        self.eps = eps
        self.weight = nn.Parameter(torch.ones(dim))

    def _norm(self, x):
        return x * torch.rsqrt(x.pow(2).mean(-1, keepdim=True) + self.eps)

    def forward(self, x):
        output = self._norm(x.float()).type_as(x)
        return output * self.weight


def precompute_freqs_cis(dim: int, end: int, theta: float = 10000.0):
    freqs = 1.0 / (theta ** (torch.arange(0, dim, 2)[: (dim // 2)].float() / dim))
    t = torch.arange(end, device=freqs.device, dtype=torch.float32)
    freqs = torch.outer(t, freqs)
    freqs_cis = torch.polar(torch.ones_like(freqs), freqs)  # complex64
    return freqs_cis


def reshape_for_broadcast(freqs_cis: torch.Tensor, x: torch.Tensor):
    ndim = x.ndim
    assert 0 <= 1 < ndim
    assert freqs_cis.shape == (x.shape[1], x.shape[-1])
    shape = [d if i == 1 or i == ndim - 1 else 1 for i, d in enumerate(x.shape)]
    return freqs_cis.view(*shape)


def apply_rotary_emb(
    xq: torch.Tensor,
    xk: torch.Tensor,
    freqs_cis: torch.Tensor,
) -> Tuple[torch.Tensor, torch.Tensor]:
    xq_ = torch.view_as_complex(xq.float().reshape(*xq.shape[:-1], -1, 2))
    xk_ = torch.view_as_complex(xk.float().reshape(*xk.shape[:-1], -1, 2))
    freqs_cis = reshape_for_broadcast(freqs_cis, xq_)
    xq_out = torch.view_as_real(xq_ * freqs_cis).flatten(3)
    xk_out = torch.view_as_real(xk_ * freqs_cis).flatten(3)
    return xq_out.type_as(xq), xk_out.type_as(xk)


def repeat_kv(x: torch.Tensor, n_rep: int) -> torch.Tensor:
    """torch.repeat_interleave(x, dim=2, repeats=n_rep)"""
    bs, slen, n_kv_heads, head_dim = x.shape
    if n_rep == 1:
        return x
    return (
        x[:, :, :, None, :]
        .expand(bs, slen, n_kv_heads, n_rep, head_dim)
        .reshape(bs, slen, n_kv_heads * n_rep, head_dim)
    )


class Attention(nn.Module):
    def __init__(self, args: ModelArgs):
        super().__init__()
        self.n_kv_heads = args.n_heads if args.n_kv_heads is None else args.n_kv_heads
        model_parallel_size = fs_init.get_model_parallel_world_size()
        self.n_local_heads = args.n_heads // model_parallel_size
        self.n_local_kv_heads = self.n_kv_heads // model_parallel_size
        self.n_rep = self.n_local_heads // self.n_local_kv_heads
        self.head_dim = args.dim // args.n_heads

        # extended memory variables
        self.enable_external_mind = args.enable_external_mind
        self.topk = args.topk
        self.mask_by_sim = args.mask_by_sim
        self.sim_threshold = args.sim_threshold
    
        self.wq = ColumnParallelLinear(
            args.dim,
            args.n_heads * self.head_dim,
            bias=False,
            gather_output=False,
            init_method=lambda x: x,
        )
        self.wk = ColumnParallelLinear(
            args.dim,
            self.n_kv_heads * self.head_dim,
            bias=False,
            gather_output=False,
            init_method=lambda x: x,
        )
        self.wv = ColumnParallelLinear(
            args.dim,
            self.n_kv_heads * self.head_dim,
            bias=False,
            gather_output=False,
            init_method=lambda x: x,
        )
        self.wo = RowParallelLinear(
            args.n_heads * self.head_dim,
            args.dim,
            bias=False,
            input_is_parallel=True,
            init_method=lambda x: x,
        )

        self.cache_k = torch.zeros(
            (
                args.max_batch_size,
                args.max_seq_len,
                self.n_local_kv_heads,
                self.head_dim,
            )
        ).cuda()
        self.cache_v = torch.zeros(
            (
                args.max_batch_size,
                args.max_seq_len,
                self.n_local_kv_heads,
                self.head_dim,
            )
        ).cuda()

    def forward(
        self,
        x: torch.Tensor,
        start_pos: int,
        freqs_cis: torch.Tensor,
        mask: Optional[torch.Tensor],
        long_range_past_key_values: Optional[Tuple[torch.Tensor, torch.Tensor]] = None, # K & V cachde
    ):
        bsz, seqlen, _ = x.shape
        xq, xk, xv = self.wq(x), self.wk(x), self.wv(x)

        xq = xq.view(bsz, seqlen, self.n_local_heads, self.head_dim)
        xk = xk.view(bsz, seqlen, self.n_local_kv_heads, self.head_dim)
        xv = xv.view(bsz, seqlen, self.n_local_kv_heads, self.head_dim)

        xq, xk = apply_rotary_emb(xq, xk, freqs_cis=freqs_cis)

        self.cache_k = self.cache_k.to(xq)
        self.cache_v = self.cache_v.to(xq)

        self.cache_k[:bsz, start_pos : start_pos + seqlen] = xk
        self.cache_v[:bsz, start_pos : start_pos + seqlen] = xv

        keys = self.cache_k[:bsz, : start_pos + seqlen]
        values = self.cache_v[:bsz, : start_pos + seqlen]

        # repeat k/v heads if n_kv_heads < n_heads
        keys = repeat_kv(
            keys, self.n_rep
        )  # (bs, cache_len + seqlen, n_local_heads, head_dim)
        values = repeat_kv(
            values, self.n_rep
        )  # (bs, cache_len + seqlen, n_local_heads, head_dim)

        # add memory 
        if self.enable_external_mind:
          if long_range_past_key_values is not None:
            
            # retrieving memory and setting head dimensions
            mem_k_cache, mem_v_cache = long_range_past_key_values # unpacking K V cache /// (bsz, memlen, n_heads, head_dim)
            mem_k_cache = repeat_kv(mem_k_cache, self.n_rep)  # duplicating K from GQA groups to match query heads
            mem_v_cache = repeat_kv(mem_v_cache, self.n_rep) # duplicating V from GQa groups to match query heads

            self.topk = min(self.topk, mem_k_cache.shape[1]) # check if mem_k_cache has less mem tokens than k

            # normalizing query & key vectors
            q_norm = (q_norm) / (torch.norm(xq, dim = -1, keepdim = True)) # normalizing head_dim components for Q
            mem_k_norm = (mem_k_cache) / torch.norm(mem_k_cache, dim = -1, keepdim = True) # normalizing head_dim components for Mem K cache
            
            # getting similarities
            similarities = torch.matmul(q_norm, mem_k_norm.transpose(2, 3)) # (bsz, seqlen, n_heads, memlen), for each query's head get each memory token's similarity

            # getting the top-k similarity values and indices
            topk_mem_sims, topk_mem_indices = torch.topk(similarities, k = self.topk, dim = -1) # shape is [bsz, seqlen, n_heads, self.topk], get top k from memlen

            # getting the topk memory tokens for all query tokens
            retrieved_k = torch.gather(mem_k_cache, dim = 1, index = (topk_mem_indices.unsqueeze(-1).expand(-1,-1,-1, self.head_dim))) # shape is [bsz, seqlen, n_heads, self.topk, head_dim] 
            retrieved_v = torch.gather(mem_v_cache, dim = 1, index = (topk_mem_indices.unsqueeze(-1).expand(-1,-1,-1, self.head_dim)))
            
            # adding this to local/internal key & value
            retrieved_k_reshaped = retrieved_k.transpose(1, 2).reshape(bsz, -1, seqlen * self.topk, self.head_dim)
            keys = torch.cat([keys, retrieved_k_reshaped], dim = 2) # shape becomes [bsz, n_heads, seq_len * topk + cache_len + seq_len, head_dim]

            retrieved_v_reshaped = retrieved_v.transpose(1, 2).reshape(bsz, -1, seqlen * self.topk, self.head_dim)
            values = torch.cat([keys, retrieved_v_reshaped], dim = 2) # shape becomes [bsz, n_heads, seq_len * topk + cache_len + seq_len, head_dim]

            # creating memory masking
            memory_mask = torch.full((bsz, self.n_local_heads, seqlen, seqlen * self.topk), float('inf'), device = keys.device)
            for t in range(seqlen):
              memory_mask[:, :, t, (t * self.topk):((t+1) * self.topk)] = 0 # for each query vector, assign 1 to its range of memory tokens
            
            if mask is not None:
              combined_mask = torch.cat([memory_mask, mask.expand(bsz, self.n_local_heads, seqlen, -1)], dim = -1)
            else:
              combined_mask = memory_mask

            # add similarity threshold ?

        xq = xq.transpose(1, 2)  # (bs, n_local_heads, seqlen, head_dim)
        keys = keys.transpose(1, 2)  # (bs, n_local_heads, cache_len + seqlen, head_dim)
        values = values.transpose(
            1, 2
        )  # (bs, n_local_heads, cache_len + seqlen, head_dim)
        scores = torch.matmul(xq, keys.transpose(2, 3)) / math.sqrt(self.head_dim)
        if combined_mask is not None:
            scores = scores + combined_mask  # (bs, n_local_heads, seqlen, cache_len + seqlen)
        scores = F.softmax(scores.float(), dim=-1).type_as(xq)
        output = torch.matmul(scores, values)  # (bs, n_local_heads, seqlen, head_dim)
        output = output.transpose(1, 2).contiguous().view(bsz, seqlen, -1)
        return self.wo(output)


class FeedForward(nn.Module):
    def __init__(
        self,
        dim: int,
        hidden_dim: int,
        multiple_of: int,
        ffn_dim_multiplier: Optional[float],
    ):
        super().__init__()
        hidden_dim = int(2 * hidden_dim / 3)
        # custom dim factor multiplier
        if ffn_dim_multiplier is not None:
            hidden_dim = int(ffn_dim_multiplier * hidden_dim)
        hidden_dim = multiple_of * ((hidden_dim + multiple_of - 1) // multiple_of)

        self.w1 = ColumnParallelLinear(
            dim, hidden_dim, bias=False, gather_output=False, init_method=lambda x: x
        )
        self.w2 = RowParallelLinear(
            hidden_dim, dim, bias=False, input_is_parallel=True, init_method=lambda x: x
        )
        self.w3 = ColumnParallelLinear(
            dim, hidden_dim, bias=False, gather_output=False, init_method=lambda x: x
        )

    def forward(self, x):
        return self.w2(F.silu(self.w1(x)) * self.w3(x))


class TransformerBlock(nn.Module):
    def __init__(self, layer_id: int, args: ModelArgs):
        super().__init__()
        self.n_heads = args.n_heads
        self.dim = args.dim
        self.head_dim = args.dim // args.n_heads
        self.attention = Attention(args)
        self.feed_forward = FeedForward(
            dim=args.dim,
            hidden_dim=4 * args.dim,
            multiple_of=args.multiple_of,
            ffn_dim_multiplier=args.ffn_dim_multiplier,
        )
        self.layer_id = layer_id
        self.attention_norm = RMSNorm(args.dim, eps=args.norm_eps)
        self.ffn_norm = RMSNorm(args.dim, eps=args.norm_eps)

    def forward(
        self,
        x: torch.Tensor,
        start_pos: int,
        freqs_cis: torch.Tensor,
        mask: Optional[torch.Tensor],
    ):
        h = x + self.attention(self.attention_norm(x), start_pos, freqs_cis, mask)
        out = h + self.feed_forward(self.ffn_norm(h))
        return out


class Transformer(nn.Module):
    def __init__(self, params: ModelArgs):
        super().__init__()
        self.params = params
        self.vocab_size = params.vocab_size
        self.n_layers = params.n_layers

        self.tok_embeddings = VocabParallelEmbedding(
            params.vocab_size, params.dim, init_method=lambda x: x
        )

        self.layers = torch.nn.ModuleList()
        for layer_id in range(params.n_layers):
            self.layers.append(TransformerBlock(layer_id, params))

        self.norm = RMSNorm(params.dim, eps=params.norm_eps)
        self.output = ColumnParallelLinear(
            params.dim, params.vocab_size, bias=False, init_method=lambda x: x
        )

        self.freqs_cis = precompute_freqs_cis(
            params.dim // params.n_heads,
            params.max_seq_len * 2,
            params.rope_theta,
        )

        self.memory_kv = [(torch.empty(0), torch.empty(0)) for layer in range(self.n_layers)] #em

    def add_to_em(self, memory_tokens : torch.Tensor):

      for layer_idx, layer in enumerate(self.layers):
        k = layer.attention.wk(memory_tokens) #[bsz, memlen, num_heads * head_dim] // check
        v = layer.attention.wv(memory_tokens) #[bsz, memlen, num_heads * head_dim] // check

        # reshaping
        k = k.view(memory_tokens.shape[0], memory_tokens.shape[1], self.n_heads, self.head_dim) # [bsz, memlen, n_heads, head_dim]
        v = v.view(memory_tokens.shape[0], memory_tokens.shape[1], self.n_heads, self.head_dim) # [bsz, memlen, n_heads, head_dim]

        # adding to memory_kv
        self.memory_kv[layer_idx] = (
            torch.cat([self.memory_kv[layer_idx][0], k], dim = 1),
            torch.cat([self.memory_kv[layer_idx][1], v], dim = 1)
        )

    def clear_em(self):
      self.memory_kv = [(torch.empty(0), torch.empty(0)) for layer in range(self.n_layers)] 

    # need to pass in memory blocks here along with enabled use
    @torch.inference_mode()
    def forward(self, tokens: torch.Tensor, start_pos: int):
        _bsz, seqlen = tokens.shape
        h = self.tok_embeddings(tokens)
        self.freqs_cis = self.freqs_cis.to(h.device)
        freqs_cis = self.freqs_cis[start_pos : start_pos + seqlen]

        mask = None
        if seqlen > 1:
            mask = torch.full((seqlen, seqlen), float("-inf"), device=tokens.device)

            mask = torch.triu(mask, diagonal=1)

            # When performing key-value caching, we compute the attention scores
            # only for the new sequence. Thus, the matrix of scores is of size
            # (seqlen, cache_len + seqlen), and the only masked entries are (i, j) for
            # j > cache_len + i, since row i corresponds to token cache_len + i.
            mask = torch.hstack(
                [torch.zeros((seqlen, start_pos), device=tokens.device), mask]
            ).type_as(h)

        # pass in memory
        for layer_idx, layer in enumerate(self.layers):
            h = layer(h, start_pos, freqs_cis, mask, self.memory_kv[layer_idx]) # passing memory for correct decoder layer
        h = self.norm(h)
        output = self.output(h).float()
        return output
